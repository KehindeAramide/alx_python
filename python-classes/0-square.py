"""Defining a class square."""

class Square:
    """Represents a square."""
def __init__(self, size):
    """Initializing a new square"""
    """the size of the new square."""
    self.__size = size
def area(self):
    """representing the area of a square"""
    return self.__size ** 2
def __str__(self):
    return f"Square with lenght {self.__size}"
   

    

    